-- =========================
-- 共通商品テーブル
-- =========================
CREATE TABLE PRODUCT (
    PRODUCT_ID NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    MAKER_ID   NUMBER NOT NULL,
    NAME       VARCHAR2(255 CHAR) NOT NULL,
    PRICE      NUMBER(10) DEFAULT 0 NOT NULL,
    STOCK      NUMBER(10) DEFAULT 0 NOT NULL,
    IMAGE      VARCHAR2(1000 CHAR),
    CONSTRAINT PK_PRODUCT PRIMARY KEY (PRODUCT_ID),
    CONSTRAINT FK_PRODUCT_MAKER FOREIGN KEY (MAKER_ID)
        REFERENCES MAKER (MAKER_ID),
    CONSTRAINT CK_PRODUCT_PRICE_NONNEG CHECK (PRICE >= 0),
    CONSTRAINT CK_PRODUCT_STOCK_NONNEG CHECK (STOCK >= 0)
);

