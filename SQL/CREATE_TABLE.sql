CREATE TABLE CPU (
    CPU_ID     NUMBER GENERATED BY DEFAULT ON NULL AS IDENTITY,
    PRODUCT_ID NUMBER NOT NULL,
    GEN_ID     NUMBER NOT NULL,
    CORE_ID    NUMBER NOT NULL,
    THREAD_ID  NUMBER NOT NULL,
    CLOCK_ID   NUMBER NOT NULL,
    CONSTRAINT PK_CPU PRIMARY KEY (CPU_ID),
    CONSTRAINT UQ_CPU_PRODUCT UNIQUE (PRODUCT_ID),
    CONSTRAINT FK_CPU_PRODUCT FOREIGN KEY (PRODUCT_ID)
        REFERENCES PRODUCT (PRODUCT_ID) ON DELETE CASCADE,
    CONSTRAINT FK_CPU_GEN FOREIGN KEY (GEN_ID)
        REFERENCES CPU_GEN (GEN_ID),
    CONSTRAINT FK_CPU_CORE FOREIGN KEY (CORE_ID)
        REFERENCES CPU_CORE (CORE_ID),
    CONSTRAINT FK_CPU_THREAD FOREIGN KEY (THREAD_ID)
        REFERENCES CPU_THREAD (THREAD_ID),
